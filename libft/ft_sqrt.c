#include "libft.h"

int	ft_sqrt(int nb)
{
	int	i;
	int	l;

	i = 0;
	l = 0;
	if (nb <= 0)
		return (0);
	while (l != nb)
	{
		i = nb - (l * l);
		if (i == 0)
		{
			return (l);
		}
		else if (i < 0)
			return (0);
		l++;
	}
	return (l);
}
