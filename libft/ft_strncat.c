#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	l;

	i = 0;
	while (s1[i] != '\0')
		i++;
	l = 0;
	while (l < n && s2[l] != '\0')
	{
		s1[i + l] = s2[l];
		l++;
	}
	s1[i + l] = '\0';
	return (s1);
}
